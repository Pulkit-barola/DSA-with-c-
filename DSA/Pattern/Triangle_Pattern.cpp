#include<iostream>
using namespace std;

int main(){
    int P;
    cout<<"Enter any number :- ";
    cin>>P;
   
    for (int i = 1; i < P+1; i++)
    {
        for (int j = 1; j < i+1; j++)
        {
            cout<<"*";

        }
        cout<<endl;
    }
    
}









